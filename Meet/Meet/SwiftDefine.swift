func Stroyboard(_ storyName:String, viewControllerId:String) -> UIViewController {
    let storyboard = UIStoryboard.init(name: storyName, bundle: Bundle.main)
    let viewController = storyboard.instantiateViewController(withIdentifier: viewControllerId)
    return viewController
}



func MainThreadAlertShow(_ msg:String,view:UIView){
    DispatchQueue.main.async {
        UITools.shareInstance().showMessage(to: view, message: msg, autoHide: true)
    }
}

func UserDefaultsGetSynchronize(_ key:String) -> String {
    if UserDefaults.standard.object(forKey: key) != nil{
        return UserDefaults.standard.object(forKey: key)! as! String
    }else{
        return ""
    }
    
}

func UserDefaultsSetSynchronize(_ value:Any, key:String) {
    UserDefaults.standard.set(value, forKey: key)
}

func TableViewRegisterNib(_ cell:String, idef:String, tableView:UITableView){
    tableView.register(UINib.init(nibName: cell, bundle: Bundle.main), forCellReuseIdentifier: idef)
}


